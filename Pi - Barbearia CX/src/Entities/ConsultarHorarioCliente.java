    private DefaultTableModel montarTabela(){
        String [] colunas = {"Barbeiro", "Horario", "Data"};
        
        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);
        List<Horario> listar = BarbeariaDAO.listarHorarioCliente();
        
        for (int i = 0; i < listar.size(); i++) {
            Horario h = listar.get(i);
            String [] linhas = {
                h.getBarbeiro(),
                h.getHorario(),
                h.getDataCorte(),
            };
            tabela.addRow(linhas);
        }
        return tabela;
    }
